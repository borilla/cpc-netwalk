;; ----------------------------------------------------------------
;; constants
;; ----------------------------------------------------------------

program_addr	equ &8000

;; ----------------------------------------------------------------
;; init
;; ----------------------------------------------------------------

		org program_addr

;; ----------------------------------------------------------------
;; subroutines
;; ----------------------------------------------------------------

maze_generate	call maze_clear
		ret

maze_clear	ld hl,maze_data
		ld de,maze_data+1
		ld bc,#00fe
		ld (hl),#00
		ldir
		ret

;; ----------------------------------------------------------------
;; data
;; ----------------------------------------------------------------

		align #100,#11
maze_data	defs #100,#00
end
